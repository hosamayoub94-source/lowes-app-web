/** Get districts for a province (alphabetical). */
export function getSyriaDistricts(province) {
  return (SYRIA_DISTRICTS[province] || []).slice().sort((a, b) => a.localeCompare(b, 'ar'));
}

/** Get neighborhoods for a district (alphabetical). */
export function getSyriaNeighborhoods(district) {
  return (SYRIA_NEIGHBORHOODS[district] || []).slice().sort((a, b) => a.localeCompare(b, 'ar'));
}
