// =============================================================
// payment — منطق موحّد لطرق الدفع (مسبق/بنكي مقابل الدفع عند الباب COD).
//
// قيم payment_method نصوص عربية/مسمّاة (لا القيمة الحرفية 'bank'):
//   تركيا: «دفع عند الباب 💵»، «دفع مسبق 💳»، «تحويل بنكي»، «Papara»
//   سوريا: «Cash on Delivery 📦»، «دفع عند الاستلام»، «Sham Cash»، «تحويل»
//
// هذا الأكسيس يجيب على سؤال: هل التحصيل مسبق/بنكي (لا COD عند الباب،
// ويُقيَّد لخزينة البنك)؟ — يُستخدم للبوليصات وتوجيه خزينة المحاسبة.
//
// ملاحظة: تصنيف «مسبق الدفع» للعمولة (شرائح البائع) يستخدم تعريفاً أضيق
// (مسبق/bank/بنك فقط) عمداً في payrollEngine + محفظة البائع — لأنه سؤال
// مختلف (استحقاق مكافأة) لا يجب تغييره دون قرار المالك.
// =============================================================
const PREPAID_METHOD_RE = /مسبق|bank|بنك|حوال|تحويل|papara|kredi|kart|sham/i;

/** هل طريقة الدفع مسبقة/بنكية (لا COD عند الباب)؟ */
export function isPrepaidMethod(paymentMethod) {
  return PREPAID_METHOD_RE.test(String(paymentMethod || ''));
}

/** توجيه خزينة المحاسبة: بنك/تحويل → محفظة البنك، وإلا الكاش. */
export const isBankMethod = isPrepaidMethod;

export default isPrepaidMethod;
